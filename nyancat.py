def mainMenu():
    while 1:
        try:
            selection = str(input(">")).upper()
            
            if selection == "BANNER":
                banner()
                break
            elif selection == "HELP":
                helpPlz()
                break
            elif selection == "QUIT" or selection == "EXIT":
                break
            else:
                print("Invalid argument \"" + selection + "\" Type HELP for usage options")
                mainMenu()
        except ValueError:
            print("Invalid argument. Type HELP for usage options")
    exit
    
